<h2>About CAT</h2>
<hr>
<p><font style="Times New Roman">CAT is an innovative platform and an ingenious initiative by IETE, BIT Mesra with the view to strengthen the concepts and logics of circuit design among students.
It is a platform which provides a weekly online competition and plenty of practise tests.
By this weekly competition , we aim to generate interest amongst students towards circuital design and logics by interesting questions which will encourage the students to think out of box and moreover apply the knowledge learnt in classrooms. It is a weekly (individual/team) event which will be held every Friday (insert time). The questions will cover a range of topics. The syllabus will be provided in advance so that the students and adequately equipped.
Practise Tests: A place to hone your skills
Try your hand at as many practise problems which will help you prepare for the weekly competition as well as help you to determine your weak areas and improve upon them.
Discussion Forum:
It is a great place for novice students to kick off. Being a part of this forum , one can clear doubts and share ideas.
The rankings system will ensure a healthy competition amongst participants and also keep tabs on individual progress.
Though these tests are primarily aimed for ECE and EEE students,we welcome all enthusiasts. For any further queries please contact our Developers mentioned below.</font></p>
